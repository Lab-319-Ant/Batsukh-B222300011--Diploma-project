function featureSets = define_feature_sets()
%DEFINE_FEATURE_SETS Declare Phase 4B metadata, input, and target columns.
%
% These definitions are intentionally separate from the output tables so
% later ML code can build input matrices without accidentally using labels,
% direct impairment flags, or scenario metadata.

featureSets.forbiddenInputColumns = {'scenario_id','scenario_name','scenario_label', ...
    'traffic_mode','sector_status','impaired_sector_id','impaired_site_id', ...
    'impaired_sector_status','is_impaired_sector','referencePowerOffset_dB', ...
    'txPowerOffset_dB','outage_flag','degradation_flag','cod_label', ...
    'overload_flag','es_candidate'};

featureSets.clustering.tableName = 'phase4b_sector_features_clustering';
featureSets.clustering.metadata = {'realization_id','scenario_id','scenario_name', ...
    'site_id','sector_id','traffic_mode'};
featureSets.clustering.inputs = {'attached_ue_count','active_ue_count', ...
    'offered_traffic_Mbps','served_traffic_Mbps','unserved_traffic_Mbps', ...
    'sector_capacity_Mbps','sector_load_ratio','mean_RSRP_dBm','median_RSRP_dBm', ...
    'mean_SINR_dB','median_SINR_dB','mean_UE_throughput_Mbps', ...
    'median_UE_throughput_Mbps','qos_satisfaction_ratio','boundary_ue_ratio', ...
    'handover_risk_score','attach_rate_sector'};
featureSets.clustering.targets = {};
featureSets.clustering.intendedModule = 'clustering';

featureSets.cod.tableName = 'phase4b_sector_features_cod';
featureSets.cod.metadata = {'realization_id','sector_id','site_id','scenario_name', ...
    'impaired_sector_id','sector_status','is_impaired_sector'};
featureSets.cod.inputs = {'attached_ue_count','active_ue_count', ...
    'offered_traffic_Mbps','served_traffic_Mbps','unserved_traffic_Mbps', ...
    'sector_load_ratio','mean_RSRP_dBm','median_RSRP_dBm','mean_SINR_dB', ...
    'median_SINR_dB','mean_UE_throughput_Mbps','median_UE_throughput_Mbps', ...
    'qos_satisfaction_ratio','boundary_ue_ratio','handover_risk_score', ...
    'attach_rate_sector'};
featureSets.cod.targets = {'cod_label'};
featureSets.cod.intendedModule = 'COD';

featureSets.tpqp.tableName = 'phase4b_network_features_tp_qp';
featureSets.tpqp.metadata = {'dataset_id','scenario_id','realization_id', ...
    'scenario_name','traffic_mode'};
% total_offered_traffic_Mbps is a TP regression target. It must therefore
% not appear as an input feature for the same module (Phase 4B network
% TP/QP table). Phase 7B trains on the temporal sector feature table with
% explicit lag features and bans any 'next_*' column as input.
featureSets.tpqp.inputs = {'active_ues', ...
    'total_served_traffic_Mbps','total_unserved_traffic_Mbps', ...
    'overloaded_sector_count','mean_sector_load','max_sector_load', ...
    'mean_best_RSRP_dBm','mean_best_SINR_dB','coverage_ratio', ...
    'boundary_ue_ratio','handover_risk_score','es_candidate_sector_count'};
featureSets.tpqp.targets = {'qos_satisfaction_ratio_active','total_offered_traffic_Mbps'};
featureSets.tpqp.intendedModule = 'TP_QP';
end
