function featureSets = define_action_value_feature_sets()
%DEFINE_ACTION_VALUE_FEATURE_SETS Declare Phase 9A roles for each column.
%
% Roles:
%   metadata               - traceability only, never a model input
%   input_feature_candidate- a permitted pre-action or action-parameter feature
%   target                 - regression target (reward)
%   evaluation_metadata    - post-hoc audit flags (oracle, safety), never inputs
%   forbidden_leakage      - explicitly forbidden as input (post-action, reward, oracle)
%   diagnostic_only        - kept for debugging, not for training

% --- shared metadata columns kept in every dataset ---------------------
featureSets.metadata = {'action_id','oracle_group_id','dataset_id', ...
    'scenario_id','realization_id','scenario_name','source_site_id', ...
    'source_sector_id','target_sector_id','module_name','action_type'};

% --- state input feature candidates (pre-action only) -----------------
featureSets.stateInputs = {'source_sector_load','target_sector_load', ...
    'source_mean_RSRP_dBm','source_mean_SINR_dB', ...
    'source_qos_satisfaction_ratio','source_handover_risk_score', ...
    'source_attach_rate_sector'};

% --- action input feature candidates ----------------------------------
featureSets.actionInputs = {'delta_prs_dB','delta_tilt_deg','delta_cio_dB', ...
    'delta_hom_dB','delta_ttt_ms','sleep_flag','is_no_op','es_action_code'};

% --- regression target ------------------------------------------------
featureSets.targets = {'reward'};

% --- evaluation metadata (post-hoc audit, never inputs) ---------------
featureSets.evaluationMetadata = {'safety_valid','oracle_selected', ...
    'oracle_selection_reason','unsafe_fallback_group','safe_training_candidate', ...
    'invalid_reason','safety_attach_loss','safety_qos_loss','safety_sinr_loss', ...
    'safety_rsrp_loss','safety_neighbor_overload','safety_handover_risk', ...
    'safety_es_sleep_impaired','safety_is_unsafe','evaluation_note'};

% --- forbidden as model input -----------------------------------------
featureSets.forbiddenInputs = {'reward','oracle_selected','oracle_reward', ...
    'oracle_selection_reason','safety_valid','unsafe_fallback_group', ...
    'safe_training_candidate', ...
    'post_source_load_ratio','post_target_load_ratio', ...
    'post_source_RSRP_dBm','post_source_SINR_dB', ...
    'post_source_qos_satisfaction_ratio','post_source_attach_rate', ...
    'post_source_handover_risk_score', ...
    'delta_source_load_ratio','delta_target_load_ratio', ...
    'delta_source_RSRP_dB','delta_source_SINR_dB', ...
    'delta_source_qos_satisfaction_ratio','delta_source_attach_rate', ...
    'delta_source_handover_risk_score', ...
    'reward_coverage_term','reward_qos_term','reward_load_term', ...
    'reward_handover_term','reward_energy_term','reward_penalty_term', ...
    'energy_delta_proxy','interference_delta_proxy','action_cost_proxy', ...
    'scenario_label','cod_label','outage_flag','degradation_flag', ...
    'impaired_sector_id','is_impaired_sector','sector_status', ...
    'kpi_t_plus_1','kpi_next','next_state_dataset'};

% --- module dataset specifications ------------------------------------
featureSets.modules = {'COC/OH','LB/MLB','ES','HO/MRO'};
featureSets.moduleFileSuffix = struct( ...
    'COC_OH', 'coc', ...
    'LB_MLB', 'lb', ...
    'ES',     'es', ...
    'HO_MRO', 'mro');

featureSets.moduleRelevantActionInputs = struct();
featureSets.moduleRelevantActionInputs.('COC_OH') = {'delta_prs_dB','delta_tilt_deg','delta_cio_dB','is_no_op'};
featureSets.moduleRelevantActionInputs.('LB_MLB') = {'delta_cio_dB','is_no_op'};
featureSets.moduleRelevantActionInputs.('ES')     = {'sleep_flag','es_action_code'};
featureSets.moduleRelevantActionInputs.('HO_MRO') = {'delta_hom_dB','delta_ttt_ms','delta_cio_dB','is_no_op'};
end
