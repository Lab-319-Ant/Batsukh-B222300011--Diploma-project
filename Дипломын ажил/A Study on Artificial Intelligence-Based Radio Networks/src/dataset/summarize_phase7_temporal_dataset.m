function summaryTable = summarize_phase7_temporal_dataset(networkTemporal)
%SUMMARIZE_PHASE7_TEMPORAL_DATASET Aggregate temporal KPIs by scenario.

scenarioNames = unique(networkTemporal.scenario_name, 'stable');
rows = {};
for i = 1:numel(scenarioNames)
    idx = strcmp(networkTemporal.scenario_name, scenarioNames{i});
    rows(end+1, :) = {scenarioNames{i}, sum(idx), ...
        mean(networkTemporal.total_offered_traffic_Mbps(idx), 'omitnan'), ...
        mean(networkTemporal.total_served_traffic_Mbps(idx), 'omitnan'), ...
        mean(networkTemporal.qos_satisfaction_ratio(idx), 'omitnan'), ...
        mean(networkTemporal.mean_sector_load(idx), 'omitnan'), ...
        mean(networkTemporal.overloaded_sector_count(idx), 'omitnan'), ...
        mean(networkTemporal.handover_risk_score(idx), 'omitnan')}; %#ok<AGROW>
end
summaryTable = cell2table(rows, 'VariableNames', {'scenario_name','time_rows', ...
    'mean_offered_traffic_Mbps','mean_served_traffic_Mbps','mean_qos_satisfaction_ratio', ...
    'mean_sector_load','mean_overloaded_sector_count','mean_handover_risk_score'});
end
